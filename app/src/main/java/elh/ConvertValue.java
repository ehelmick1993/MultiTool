package elh;

public class ConvertValue {

    public double cmToInch(double cm){
        return cm / 2.54;

    }

    public double cmToMeter(double  cm){
        return cm / 100;

    }

    public double cmToFeet(double  cm){
        return cm / 30.48;

    }

    public double cmToKilometer(double cm){
        return cm / 100000;

    }

    public double cmToMile(double cm){
        return cm / 160934;

    }


    public double inchToCm(double inch){
        return inch * 2.54;

    }

    public double inchToMeter(double inch){
        return inch / 39.37;

    }

    public double inchToFeet(double inch){
        return inch / 12;

    }

    public double inchToKilometer(double inch){
        return inch / 39370;

    }
    public double inchToMile(double inch){
        return inch / 63360;

    }


}
